#include "libft.h"
#include <string.h>

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	len1;
	size_t	len2;
	char	*res;

	if (!s1 || !s2)
		return (NULL);
	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	res = ft_strnew(len1 + len2);
	if (!res)
		return (NULL);
	ft_strcpy(res, s1);
	ft_strcpy(res + len1, s2);
	return (res);
}
