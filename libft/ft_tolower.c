#include "libft.h"

int		ft_tolower(int c)
{
	const unsigned char cc = (unsigned char)c;

	return ((cc >= 'A' && cc <= 'Z' && (int)cc == c)
		? (int)(cc - 'A' + 'a') : c);
}
